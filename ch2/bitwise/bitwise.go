package main

func main() {
	// ? bitwise 操作符

	// 1. 验证websocket数据是否传输完成

	// 2. 进行二进制运算
	a := 60    // 60 = 0011 1100
	b := 13    // 13 = 0000 1101
	_ = a & b  // AND  = 0000 1100 = 12
	_ = a | b  // OR   = 0011 1101 = 61
	_ = a ^ b  // XOR  = 0011 0001 = 49
	_ = a &^ b // AND NOT = 0011 0000 = 48
}
